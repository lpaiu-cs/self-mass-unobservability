	character*(*) sccsid
	parameter (sccsid = "@(#)maknorm10.f	1.5\t01/06/92")

	INCLUDE 'COMARG.INC'

	INCLUDE 'PASSIT.INC'

	INCLUDE 'NPHLD.INC'

        INTEGER RMODE
        DOUBLE PRECISION FRSTIM,LASTIM
	double precision skew, kurtosis, PmM

C  FIT THE DATA TO CREATE A NORMALPOINT
        RMODE= 2
        CALL LSQ(TIM,RESID,NPTS,RMODE,sigmult,A,B,C,SD,SE,
     &		skew,kurtosis,PmM,NREJ)

C  GET NORMALPOINT TIME (AVERAGE FROM ALL 'GOOD' RETURNS
        NPTOT= NPTS+ NREJ
        TIMTOT= 0.D0
        FRSTIM= 0.D0
        LASTIM= 0.D0
C		SAME AS NPTS WAS
        DO 20 I=1,NPTOT
	IF (TIM(I) .LE. 0.D0) GO TO 20
          TIMTOT= TIMTOT+ TIM(I)
          IF (FRSTIM .EQ. 0.D0) FRSTIM= TIM(I)
          LASTIM= TIM(I)
 20     CONTINUE

C  NORMALPOINT TIME AND RESIDUAL
        TJDINT= JDI
        SUTCT= TIMTOT/NPTS
        OMC= A+ B*SUTCT+ C*SUTCT*SUTCT
	IF (SUTCT .LT. 86400.D0) GO TO 30
          SUTCT= SUTCT- 86400.D0
          TJDINT= TJDINT+ 1.D0

C.. TO CALCULATE VARIOUS QUANTITIES. NEEDED FOR RUN-LONG CAL STATISTICS.
C  NOTE: Uncert and calacc not used in CRD version; uncert used in summary 
 30	continue
        calacc= 0.d0
        UNCERT= DSQRT(SBYS*SBYS/NPTS+ CALACC*CALACC+ SD*SD/NPTS)

C...AND THE NORMALPOINT TIME SPAN (IN SEC)
        SPAN= LASTIM- FRSTIM
C
C	   THE
	   END

