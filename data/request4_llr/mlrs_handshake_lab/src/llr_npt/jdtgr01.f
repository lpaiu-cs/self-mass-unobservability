C  CALCULATE THE GREGORIAN DATE FROM JULIAN DATE.
C
C  REVISIONS:
C	07/27/87 - PREVENT THIS RTN FROM MODIFYING 'JDF'.  V01. RLR.
C
C$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$

	character sccsid*(*)
	parameter (sccsid = "@(#)jdtgr01.f	1.2\t06/13/89")

      DOUBLE PRECISION JDINT,JDF,TDAY,JDA1900,JDFC
      INTEGER YEAR,MONTH,DAY,HOUR,MINUTE
C
      JDFC = JDF + (JDINT-DINT(JDINT)) + 0.5D0
      JDA1900 = DINT(JDINT) - 2415079.0D0
2	IF (JDFC.LT.1.D0) GO TO 5
        JDFC  = JDFC - 1.D0
        JDA1900= JDA1900 + 1.D0
        GO TO 2

 5    HOUR   = IDINT(JDFC*24.D0)
      MINUTE = IDINT((JDFC - HOUR/24.D0)*1440.D0)
      SECOND = (JDFC - HOUR/24.D0 - MINUTE/1440.D0)*86400.D0
      YEAR   = IDINT(((4.D0*JDA1900)-1)/1461.D0)
 
      TDAY   = DINT(((4.D0*JDA1900)+3.D0- (YEAR*1461.D0))/4.D0)
      MONTH  = IDINT(((5.D0*TDAY)-3.D0)/153.D0)
      DAY    = IDINT(((5.D0*TDAY)+2.D0 - (153.D0*MONTH))/5.D0)
 
      IF(MONTH.LT.10) GO TO 10
         MONTH = MONTH - 9
         YEAR  = YEAR + 1
         GO TO 99
 10      MONTH = MONTH + 3
 
 99   CONTINUE
C
C	   THE
	   END

