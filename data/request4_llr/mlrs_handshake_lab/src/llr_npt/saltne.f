	SUBROUTINE SALTNE(AZ,ALT,NS,EW)
	IMPLICIT DOUBLE PRECISION (A-H,O-Z)
      INCLUDE 'LICENSE-BSD3.inc'
CCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCC
C  Thu Oct 20, 1988 16:12:20
C     1) REPLACE "COMPILER DOUBLE PRECISION" WITH
C        "IMPLICIT DOUBLE PRECISION (A-H,O-Z)".
CCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCC
C  Mon Oct 31, 1988 15:19:46
C     1) CHANGE TYPE REAL TO TYPE DOUBLE PRECISION.
CCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCC
C  CONVERT FROM AZ/ALT TO ALT/ALT (NORTH-SOUTH/EAST-WEST) COORDINATE SYSTEM.
C  (S)TAND ALONE VERSION DOES NOT USE TELCOR.  USER MUST ADD AZOFF & ALTOFF
C  TO AZ & ALT BEFORE CALLING THIS ROUTINE.
C
C  INPUT:  AZ -- AZIMUTH POSITION IN RADIANS,
C	   ALT -- ALTITUDE POSITION IN RADIANS.
C	   [AZOFF -- ANGLE BETWEEN TELESCOPE POLAR AXIS & TRUE EAST]
C	   [ALTOFF-- ANGLE BETWEEN TELESCOPE POLAR AXIS & 0 DEG ALT]
C
C  OUTPUT: NS -- NORTH-SOUTH AXIS POSITION IN RADIANS,
C	   EW -- EAST-WEST AXIS POSITION IN RADIANS.
C
C$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$RLR

	character*(*) sccsid
	parameter (sccsid = "@(#)saltne.f	1.1\t06/08/89")

	DOUBLE PRECISION NS,EW

	CALL POTDC(1,AZ,ALT,X,Y,Z)
	XP =  Z
	YP =  X
	ZP =  Y
	CALL DCTPO(1,XP,YP,ZP,NS,EW)
C
C	   THE
	   END
