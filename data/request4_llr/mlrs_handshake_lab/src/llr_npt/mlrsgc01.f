	CALL SALTNE(AZ,EL,NS,EW)

	C3= -((-0.003540d0)*DSIN(NS)- (-0.002147d0)*DCOS(NS))
	MLRSGCT= -(2.D0*176.1D0*DSIN(EW+ 0.04328D0+ C3))/29.97925D0

