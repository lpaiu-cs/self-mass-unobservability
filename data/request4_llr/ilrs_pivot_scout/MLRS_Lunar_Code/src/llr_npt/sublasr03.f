	SUBROUTINE SUBLASR(TJDINT,SUTCT,VLITE,UT1C,XPOLE,YPOLE,EOSRC,
     1		TTLTIM,LHATRN,AZALTRN,LHARCV,AZALRCV)
	IMPLICIT DOUBLE PRECISION (A-H,O-Z)
      INCLUDE 'LICENSE-BSD3.inc'
