C  CALCULATE ROUND-TRIP RANGE BETWEEN EARTH SITE AND LUNAR REFLECTOR
C
C  REVISIONS:
C	07/21/87 - UPDATE /MODINFO/ & /PDCMN/. V02. RLR.
C	09/01/87 - GET EARTH ORIENTATION SOURCE FROM INITRNG. V03. RLR.
C
C$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$ RLR 10/85

	character*(*) sccsid
	parameter (sccsid = "@(#)sublasr03.f	1.3\t05/02/90")

	INCLUDE 'DEFCMN.INC'

	INCLUDE 'INTRNG.INC'

	INCLUDE 'OBSINF.INC'

	INCLUDE 'MODINF.INC'

	INCLUDE 'PDCMN.INC'

	INCLUDE 'DEVCOM.INC'

	INCLUDE 'BOUNCE.INC'

	INCLUDE 'DIRCTN.INC'

      DOUBLE PRECISION STVTR(6)
      DOUBLE PRECISION LHATRN(6),AZALTRN(6),LHARCV(6),AZALRCV(6)
      CHARACTER*1 EOSRC

C-----------------------------------------------------------------
C:INITIALIZATION FOR RANGE COMPUTATION
      CALL INITRNG(TJDINT,SUTCT,ITRANS,IRECVR,POLMOTN,AUKM,
     $ STVTR,UT1C,EMUTC,XPOLE,YPOLE,EOSRC,DEPS,DPSI,DTADT)

      DO 300 I=1,6
  300 STVEC(I)=STVTR(I)

C:OUT BOUNCE ITERATION
       CALL OUTLEG(TJDINT,SUTCT,BODRFL,ITERMAX,EMUTC,
     1		K2MC3,VLITE,TOLERR,DT12,DIST12,CORR12,SUTCB)

C:IF TWO WAY RANGE NEEDED
      IF (.NOT.TWOWAY) GO TO 350

C:BOUNCE RETURN ITERATION
      CALL BAKLEG(TJDINT,SUTCB,BODRCV,ITERMAX,UT1C,EMUTC,
     1		DEPS,DPSI,K2MC3,VLITE,TOLERR,DT12,DIST23,CORR23)
C:ENDIF

C:COMPUTE TWO WAY LIGHT TRAVEL TIME AND APPLY NECESSARY CORRECTIONS
 350  CALL LITTIM(DIST12,DIST23,AUKM,VLIGHT,
     1		CORR12,CORR23,DTADT,TTLTIM)

C:COMPUTE POINTING ANGLES (TRANSMITTER)
      LEGDIR=1
      CALL POINT(TRFVEC,TRANSM,TGDLAT,DIST12,VLITE,LHATRN,AZALTRN)

C:IF TRANSMITTER NOT RECEIVER
      IF (ITRANS.EQ.IRECVR) GO TO 375

C:COMPUTE POINTING ANGLES (RECEIVER)
      LEGDIR=2
      CALL POINT (RFRVEC,RECEIV,RGDLAT,DIST23,VLITE,LHARCV,AZALRCV)
C:ENDIF

 375	CONTINUE
C
C	   THE
	   END

