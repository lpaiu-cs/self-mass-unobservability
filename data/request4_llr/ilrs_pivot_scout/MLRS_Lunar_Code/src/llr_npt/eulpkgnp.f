C:CONVERT THE OBLIQUITY TO RADIANS BY MULTIPLYING BY/
C:TWOPI
C:CONVERT THE RATE OF THE OBLIQUITY TO RADIANS PER DAY/
C:BY MULTIPLYING BY TWOPI AND DIVIDING BY 365.25
C
C***NOTE: AS CODED, THIS ROUTINE (FOR COMPATABILITY WITH THE UNIV OF TX LLR
C       ANALYSIS PACKAGE, MAINLY 'SUBLASER') ASSUMES THAT ARBITRARY FIXED
C       EPOCH IS 1950.0 TO CONFORM WITH JPL DE-111 AND EARLIER EPHEMERIDES.
C       AT SUCH TIME THAT EPOCH 2000.0 EPHEMERIDES ARE USED A CHANGE TO T1
