	SUBROUTINE RECTNG(SPHERE,XYZ)
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
C:%S COMPUTE RECTANGULAR COORDINATES FROM SPHERICAL COORDINATES
C:COMPUTE SIN AND COS OF LONGITUDE
C:COMPUTE SIN AND COS OF LATITUDE
C:COMPUTE QUANTITY COS(LAT) COS(LONG)
C:COMPUTE QUANTITY COS(LAT) SIN(LONG)
C:COMPUTE QUANTITY SIN(LAT) COS(LONG)
C:COMPUTE QUANTITY SIN(LAT) SIN(LONG)
C:COMPUTE X = R COS(LAT) COS(LONG)
C:COMPUTE Y = R COS(LAT) SIN(LONG)
C:COMPUTE Z = R COS(LAT)
C:(NOW THE RATES OF X, Y AND Z)
C:COMPUTE XD = RD COS(LAT) COS(LONG) - R (LONGD COS(LAT) SIN(LONG) +/ 
C: LATD SIN(LAT) COS(LONG))
C:COMPUTE YD = RD COS(LAT) SIN(LONG) + R (LONGD COS(LAT) COS(LONG) -/ 
C: LATD SIN(LAT) SIN(LONG))
C:COMPUTE ZD = RD SIN(LAT) + R LATD COS(LAT)
      DOUBLE PRECISION SPHERE(6),XYZ(6) 
      SINLON = DSIN(SPHERE(2))
      COSLON = DCOS(SPHERE(2))
      SINLAT = DSIN(SPHERE(3))
      COSLAT = DCOS(SPHERE(3))
      CC = COSLAT*COSLON
      CS = COSLAT*SINLON
      SC = SINLAT*COSLON
      SS = SINLAT*SINLON
      XYZ(1) = SPHERE(1)*CC
      XYZ(2) = SPHERE(1)*CS
      XYZ(3) = SPHERE(1)*SINLAT
      XYZ(4) = SPHERE(4)*CC - SPHERE(1)*(SPHERE(5)*CS+SPHERE(6)*SC)
      XYZ(5) = SPHERE(4)*CS + SPHERE(1)*(SPHERE(5)*CC-SPHERE(6)*SS)
      XYZ(6) = SPHERE(4)*SINLAT + SPHERE(1)*SPHERE(6)*COSLAT
C
C	   THE
	   END


