      SUBROUTINE ROTMAT(KAXIS,A,R)
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
C:%S CONSTRUCT AUGMENTED ROTATION MATRIX
C:(INPUT IS ROTATION FLAG, ANGLE AND ANGLE RATE; OUTPUT IS ROTATION/
C:MATRIX)
C:INITIALIZE 6X3 ROTATION MATRIX TO ZERO
C:IF ROTATION FLAG IS NEGATIVE
C:MULTIPLY ANGLE BY - 1
C:MULTIPLY ANGLE RATE BY - 1
C:ENDIF
C:COMPUTE SIN ANGLE
C:COMPUTE COS ANGLE
C:MULTIPLY ANGLE RATE BY SIN ANGLE
C:MULTIPLY ANGLE RATE BY COS ANGLE
C:IF ABSOLUTE VALUE OF ROTATION FLAG EQUAL TO 1
C:(MATRIX FOR ROTATION ABOUT THE 1 AXIS)
C:SET 1,1 ELEMENT TO 1.0
C:SET 2,2 ELEMENT TO COS ANGLE
C:SET 2,3 ELEMENT TO - SIN ANGLE
C:SET 3,2 ELEMENT TO SIN ANGLE
C:SET 3,3 ELEMENT TO COS ANGLE
C:SET 5,2 ELEMENT TO - ANGLE RATE X SIN ANGLE
C:SET 5,3 ELEMENT TO - ANGLE RATE X COS ANGLE
C:SET 6,2 ELEMENT TO ANGLE RATE X COS ANGLE
C:SET 6,3 ELEMENT TO - ANGLE RATE X SIN ANGLE
C:ELSEIF ABSOLUTE VALUE OF ROTATION FLAG EQUAL TO 2
C:(MATRIX FOR ROTATION ABOUT THE 2 AXIS)
C:SET 2,2 ELEMENT TO 1.0
C:SET 1,1 ELEMENT TO COS ANGLE
C:SET 1,3 ELEMENT TO SIN ANGLE
C:SET 3,1 ELEMENT TO - SIN ANGLE
C:SET 3,3 ELEMENT TO COS ANGLE
C:SET 4,1 ELEMENT TO - ANGLE RATE X SIN ANGLE
C:SET 4,3 ELEMENT TO ANGLE RATE X COS ANGLE
C:SET 6,1 ELEMENT TO - ANGLE RATE X COS ANGLE
C:SET 6,2 ELEMENT TO - ANGLE RATE X SIN ANGLE
C:ELSE
C:(MATRIX FOR ROTATION ABOUT THE 3 AXIS)
C:SET 3,3 ELEMENT TO 1.0
C:SET 1,1 ELEMENT TO COS ANGLE
C:SET 1,2 ELEMENT TO - SIN ANGLE
C:SET 2,1 ELEMENT TO SIN ANGLE
C:SET 2,2 ELEMENT TO COS ANGLE
C:SET 4,1 ELEMENT TO - ANGLE RATE X SIN ANGLE
C:SET 4,2 ELEMENT TO - ANGLE RATE X COS ANGLE
C:SET 5,1 ELEMENT TO ANGLE RATE X COS ANGLE
C:SET 5,2 ELEMENT TO - ANGLE RATE X SIN ANGLE
C:ENDIF
C  Revisions:
C	11/22/88 - condense the program by recognizing
c		   patterns in rotations. v01. rlr.
C
C$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$
c 21 Sep 90, 0935: use d.p. constants; use d.p. functions dsin, dcos
ccccccccccccccccccccccccccccccccccccccccccccccccccccccccccccccccccccc

      DIMENSION R(6,3),A(2)
      
      DO 1 J = 1,6
      DO 1 I = 1,3
    1 R(J,I) = 0.0d0
    
      K= IABS(KAXIS)
      IONE= K/KAXIS
      A1= IONE*A(1)
      ADOT= IONE*A(2)
      
      SINA = dSIN(A1)
      COSA = dCOS(A1)
      ADSINA = ADOT*SINA
      ADCOSA = ADOT*COSA

      KP1= MOD(K,3)+ 1
      KP2= MOD(KP1,3)+ 1
      R(K,K)= 1.0d0
      R(KP1,KP1)= COSA
      R(KP1,KP2)= -SINA
      R(KP2,KP1)= SINA
      R(KP2,KP2)= COSA
      R(KP1+3,KP1)= -ADSINA
      R(KP1+3,KP2)= -ADCOSA
      R(KP2+3,KP1)= ADCOSA
      R(KP2+3,KP2)= -ADSINA
c
c	   the
	   end


