	SUBROUTINE UT1TIDE(K2LOVE,DUT1)
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
C:%S COMPUTE CORRECTION TO UT1 FOR PERIODIC TERMS DUE TO TIDAL BULGE
C:(INPUT IS THE K2 LOVE NUMBER AND ALSO THROUGH THE COMMON OF THE/
C:FUNDAMENTAL ARGUMENTS: ANGLE OF ASCENDING NODE AND MEAN ANOMALY OF/ 
C:MOON, LUNAR ARGUMENT OF LATITUDE, MEAN ANOMALY OF SUN AND THE MEAN/ 
C:ELONGATION OF THE MOON FROM THE SUN ALL IN DEGREES! OUTPUT IS THE/
C:CORRECTION TO UT1 IN SECONDS)
C:COMPUTE THE TIDAL BULGE CORRECTION TO UT1 FROM ITS EXPANSION
C  REF: YODER, ET AL., J. GEOPHYS. ES. 86, 881-891, 1981.

        DOUBLE PRECISION K2LOVE
 
	INCLUDE 'DEFCMN.INC'

	INCLUDE 'DEVCOM.INC'

	INCLUDE 'FUNBLK.INC'
 
C  QUASI-MONTHLY TERMS: 27.9->34.9 DAYS 
      QMTIDE=(.0437D0*DSIN( ( EMM + 2.D0*F +      EOM))
     X       +.1056D0*DSIN( ( EMM + 2.D0*F + 2.D0*EOM))
     X       +.0210D0*DSIN( (-EMM + 2.D0*F + 2.D0*EOM + 2.D0*D)) 
     X       +.0318D0*DSIN( (       2.D0*F           ))
     X       +.3413D0*DSIN( (       2.D0*F +      EOM))
     X       +.8252D0*DSIN( (       2.D0*F + 2.D0*EOM))
     X       +.0781D0*DSIN( (2.D0*D))
     X       +.0106D0*DSIN( (EOM)) * DCOS( (2.D0*D))
     X       +.0360D0*(1.D0 - .12D0*DCOS( (EOM))) * DSIN( (2.D0*EMM)))
     X          *1.D-3
 
C  QUASI-WEEKLY TERMS: 5.6->15.4 DAYS
      QWTIDE=   (-.0188D0*DSIN( (-EMM + 2.D0*F +      EOM))
     X           -.0463D0*DSIN( (-EMM + 2.D0*F + 2.D0*EOM))
     X           +.8788D0*(1. - .131D0*DCOS( (EOM))) * DSIN( ( EMM))
     X           +.1940D0*(1. - .137D0*DCOS( (EOM))) *
     4					DSIN( (-EMM + 2.D0*D)))
     X           *1.D-3
 
C  3.2...=MOMENTUM->LOVE CONVERSION RATIO
      DUT1 = -K2LOVE * 3.24138D0 * (QMTIDE + QWTIDE)
        IF (DEB(11)) WRITE(DUNIT,1000) DUT1
 1000   FORMAT(' UT1TIDE: DUT1 ',E20.10)
        RETURN
C
C	   THE
	   END


