	INCLUDE 'DEVCOM.INC'
 
 
      DOUBLE PRECISION JD2000 
 
      COMMON/TWOPI/TWOPI,CDR,CASR,CTSR
 
      DATA JD2000 /2451545.D0/
      DATA TCENT  /36525.D0/
      DATA T1     /2433282.423D0/
 
      T0 = (T1-JD2000)/TCENT
      T  = (XJD-T1)/TCENT
      EPS0 = 84381.448D0
      A0 = ((0.001813D0*T0-0.00059D0)*T0-46.8150D0)*T0
      A1 = (0.005439D0*T0-0.00117D0)*T0-46.8150D0
      A2 = 0.005439D0*T0-0.00059D0
      A3 = 0.001813D0
      EPSLN(1) = (((A3*T+A2)*T+A1)*T+A0+EPS0)*CASR
      EPSLN(2) =  ((3.*A3*T + 2.*A2)*T+A1)/TCENT*CASR
 
      IF (DEB(14)) WRITE(DUNIT,1000) T,EPSLN
 1000 FORMAT(1X,'OBLIQ: T & EPSLN',3E20.10)
      RETURN
C
C	   THE
	   END

