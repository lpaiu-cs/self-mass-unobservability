C  AND PRINTS THEM WITH THE HA & DEC COORDINATES.  THESE VALUES CAN BE
C  USED FOR 107" LUNAR POINTING
C
C$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$ RLR 6/83 $$$$$
