	SUBROUTINE MATMUL(A,B,C,NROWS,NCOLA,NCOLB)
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
      DOUBLE PRECISION A(6,3),B(6,3),C(6,3)
      DOUBLE PRECISION P(3,3),V(3,3)
C
      NRW2=NROWS/2
      IMAX=NRW2
      JMAX=NCOLB
      KMAX=NCOLA
C
      DO 30 J=1,JMAX
      DO 30 I=1,IMAX
      P(I,J)=0.0
      V(I,J)=0.0
      DO 30 K=1,KMAX
      M1=I
      M2=K
      M3=K
      M4=J
      P(I,J)=P(I,J) + A(M1,M2)*B(M3,M4) 
      V(I,J)=V(I,J) + A(M1,M2)*B(M3+3,M4) + A(M1+3,M2)*B(M3,M4)
   30 CONTINUE
      DO 40 J=1,JMAX
      DO 40 I=1,IMAX
      C(I,J)=P(I,J) 
   40 C(I+3,J)=V(I,J)
      RETURN
C
C	   THE
	   END


