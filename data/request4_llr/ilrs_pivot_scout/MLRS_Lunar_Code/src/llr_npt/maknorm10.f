C  DO THE FIT AND EXTRACT NORMALPOINT TIME, RESIDUAL, AND STATISTICS
C
C$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$ RLR

	character*(*) sccsid
	parameter (sccsid = "@(#)maknorm10.f	1.5\t01/06/92")

	INCLUDE 'COMARG.INC'

	INCLUDE 'PASSIT.INC'

	INCLUDE 'NPHLD.INC'

        INTEGER RMODE
        DOUBLE PRECISION FRSTIM,LASTIM

C  FIT THE DATA TO CREATE A NORMALPOINT
        RMODE= 2
        CALL LSQ(TIM,RESID,NPTS,RMODE,sigmult,A,B,C,SD,SE,NREJ)

C  GET NORMALPOINT TIME (AVERAGE FROM ALL 'GOOD' RETURNS
        NPTOT= NPTS+ NREJ
        TIMTOT= 0.D0
        FRSTIM= 0.D0
        LASTIM= 0.D0
C		SAME AS NPTS WAS
        DO 20 I=1,NPTOT
	IF (TIM(I) .LE. 0.D0) GO TO 20
          TIMTOT= TIMTOT+ TIM(I)
          IF (FRSTIM .EQ. 0.D0) FRSTIM= TIM(I)
          LASTIM= TIM(I)
 20     CONTINUE

C  NORMALPOINT TIME AND RESIDUAL
        TJDINT= JDI
        SUTCT= TIMTOT/NPTS
        OMC= A+ B*SUTCT+ C*SUTCT*SUTCT
	IF (SUTCT .LT. 86400.D0) GO TO 30
          SUTCT= SUTCT- 86400.D0
          TJDINT= TJDINT+ 1.D0

C  NORMALPOINT UNCERTAINTY ESTIMATE
C..USE FIRST AND SECOND MOMENTS OF K (Fire K) AND K' (Return K)
C.. TO CALCULATE VARIOUS QUANTITIES. NEEDED FOR RUN-LONG CAL STATISTICS.
 30	continue
	IF (NFK .EQ. 0) GO TO 33
	FKSD= DSQRT((FKSX2- FKSX*FKSX/NFK)/(NFK-1.D0))
	  DNFK = NFK
	FKSE= FKSD/DSQRT(DNFK)
c  To avoid problems when NRK is zero, test NRK, and set RKSE and RKSD to
c  zero and skip the offending divide if NRK is in fact zero.
	rksd = 0
	rkse = 0
	if(nrk .EQ. 0) goto 32
	RKSD= DSQRT((RKSX2- RKSX*RKSX/NRK)/(NRK-1.D0))
	  DNRK = NRK
	RKSE= RKSD/DSQRT(DNRK)
32	continue
C Don't recompute shot-by-shot uncert. It's not currently accurate, and unneded.
CC	SBYS= DSQRT(FKSD*FKSD+ RKSD*RKSD)
	CALACC= DSQRT(FKSE*FKSE+ RKSE*RKSE)
	GO TO 35

C  FOR NON-MLRS SYSTEMS, FOR WHICH WE HAVE NO MOMENTS:
 33     CALACC= UNCERT- SBYS

C NOW CALCULATE THE OBSERVATIONAL UNCERTAINTY
 35     UNCERT= DSQRT(SBYS*SBYS/NPTS+ CALACC*CALACC+ SD*SD/NPTS)

C...AND THE NORMALPOINT TIME SPAN (IN SEC)
        SPAN= LASTIM- FRSTIM
C
C	   THE
	   END

